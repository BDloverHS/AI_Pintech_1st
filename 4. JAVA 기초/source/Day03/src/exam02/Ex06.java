package exam02;

public class Ex06 {
    // 정수이므로 기본값 0이 들어감
    static int num1;
    // 실수이므로 기본값 0.0이 들어감
    static double num2;
    // 참 거짓의 기본값은 false
    static boolean result;
    // 문자열의 기본값은 null
    static String str;

    public static void main(String[] args) {
        System.out.println(num1);
        System.out.println(num2);
        System.out.println(result);
        System.out.println(str);
    }
}
